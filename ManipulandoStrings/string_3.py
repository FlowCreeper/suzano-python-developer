nome = "Caio David de Sousa Coutinho"

# Fatiamento de Strings

print(nome[0]) # Pega o caracter na posição 0
print(nome[-1]) # Pega o último caracter (invertido)
print(nome[:9]) # Pega os caracteres até o 9° (inclusivo)
print(nome[10:]) # Pega os caracteres a partir do 10° (exclusivo)
print(nome[10:16]) # Pega a sequencia de caracteres do 10° ao 16° seguindo a mesma lógica dos sozinhos
print(nome[10:16:2]) # Pega o mesmo intervalo do de cima, porém com passo 2
print(nome[:]) # Pega todo o conteudo da string
print(nome[::-1]) # Inverte a string pois o passo está invertido
