produto_1 = 10
produto_2 = 20

print(produto_1 + produto_2) # Soma
print(produto_1 - produto_2) # Subtração
print(produto_1 / produto_2) # Divisão
print(produto_1 // produto_2) # Divisão inteira
print(produto_1 * produto_2) # Multiplicação
print(produto_1 % produto_2) # Resto da divisão
print(produto_1 ** produto_2) # Potenciação

x = 10 + 5 * 4 # Em python, a ordem de precedência é a mesma da matemática
y = (10 / 2) + (25 * 2) - (100 ** 2) # É uma boa prática usar parênteses para deixar o código mais legível
# O resultado é o mesmo, mas fica mais fácil de entender
print(x, y)