# Atribuição simples

saldo = 500 # A variável saldo recebe o valor 500

print(saldo) # 500

saldo = 200 # Alterando o valor da variável saldo

print(saldo) # 200

# Atribuição composta

saldo += 10 # Adiciona 10 ao saldo (saldo = saldo + 10)
print(saldo) # 210

saldo -= 5 # Subtrai 5 do saldo (saldo = saldo - 5)
print(saldo) # 205

saldo //= 2 # Divide o saldo por 2 e arredonda para baixo (saldo = saldo // 2)
print(saldo) # 102

saldo /= 2 # Divide o saldo por 2 (saldo = saldo / 2)
print(saldo) # 51.0

saldo *= 10 # Multiplica o saldo por 10 (saldo = saldo * 10)
print(saldo) # 510.0

saldo %= 4 # Resto da divisão do saldo por 4 (saldo = saldo % 4)
print(saldo) # 2.0

saldo **= 2 # Potencia o saldo (saldo = saldo ** 2)
print(saldo) # 4.0
